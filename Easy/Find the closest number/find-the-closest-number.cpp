class Solution{
    public:
    int findClosest( int n, int k,int arr[]) 
    { 
        int mini=INT_MAX;
        int close=arr[0];
        for(int i=0;i<n;i++){
            if(abs(arr[i]-k)<=mini){
                mini=abs(arr[i]-k);
                close=arr[i];
            }
        }
        return close;
    } 
};

